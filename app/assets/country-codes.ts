// International dialing codes for every country/territory in the world.
// Used by the Phone Number field to pick a country calling code (lada).
// `code3` is the ISO 3166-1 alpha-3 code shown in the dropdown.
export interface CountryCode {
  name: string
  iso: string
  code3: string
  dial: string
}

// Converts a 2-letter ISO country code into its flag emoji by mapping each
// letter to its Regional Indicator Symbol. Rendering depends on OS emoji
// support, but works on macOS/iOS and most modern platforms.
export function flagEmoji(iso: string): string {
  return iso
    .toUpperCase()
    .replace(/[A-Z]/g, (char) => String.fromCodePoint(127397 + char.charCodeAt(0)))
}

