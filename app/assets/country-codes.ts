// These appear first, in this exact order. The rest follow alphabetically by
// their 3-letter code.
const PINNED_CODES = ['MEX', 'USA', 'CAN', 'ESP', 'COL']

const pinned = PINNED_CODES.map(
  (code) => RAW_COUNTRIES.find((country) => country.code3 === code)!,
)

const rest = RAW_COUNTRIES.filter((country) => !PINNED_CODES.includes(country.code3)).sort((a, b) =>
  a.code3.localeCompare(b.code3),
)

export const COUNTRY_CODES: CountryCode[] = [...pinned, ...rest]
